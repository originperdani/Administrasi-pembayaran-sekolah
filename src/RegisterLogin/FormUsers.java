     private void tampilkanDataUser() {
    try {
        Connection conn = DatabaseConnection.connect();
        String sql = "SELECT id, username, akses FROM users ORDER BY id";

        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Username");
        model.addColumn("Akses");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("username"),
                rs.getString("akses").equals("staff") ? "Staff Administrasi" : "Siswa"
            });
        }

        jTable1.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
     
    private int generateNewId(String roleType) {
    // Pindahkan deklarasi startingId ke luar blok try
    int startingId = roleType.equalsIgnoreCase("staff") ? 20001 : 10001;
    
    try (Connection conn = DatabaseConnection.connect()) {
        // Cari ID terakhir untuk role tersebut
        String sql = "SELECT MAX(id) FROM users WHERE akses = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, roleType.toLowerCase());
        ResultSet rs = stmt.executeQuery();
        
        if (rs.next() && rs.getInt(1) >= startingId) {
            return rs.getInt(1) + 1; // Increment ID terakhir
        } else {
            return startingId; // Kembalikan ID awal jika belum ada data
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error generating ID: " + e.getMessage());
        return startingId; // Sekarang startingId bisa diakses
    }
}
     
     

