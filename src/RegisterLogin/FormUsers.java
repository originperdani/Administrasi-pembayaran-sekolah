    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
    String username = jTextField1.getText().trim();
    String rawPassword = new String(jPasswordField1.getPassword()).trim();
    String selectedRole = (String)jComboBox1.getSelectedItem();

    if (username.isEmpty() || rawPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong");
        return;
    }
    
    if(rawPassword.length() < 6) {
        JOptionPane.showMessageDialog(this, "Password minimal 6 karakter.");
        return;
    }
    
    // Dalam mode pendaftaran, role dipaksa menjadi "Siswa"
    String dbRole = isRegistrationMode ? "siswa" : 
                   (selectedRole.equals("Staff Administrasi") ? "staff" : "siswa");
    String password = HashUtil.hashPassword(rawPassword);

    try (Connection conn = DatabaseConnection.connect()) {
        // Periksa apakah username sudah ada
        PreparedStatement cekStmt = conn.prepareStatement("SELECT COUNT(*) FROM users WHERE username = ?");
        cekStmt.setString(1, username);
        ResultSet rs = cekStmt.executeQuery();
        rs.next();
        
        if (rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "Username sudah digunakan!");
            return;
        }
        
        // Generate ID baru berdasarkan role
        int newId = generateNewId(dbRole);
        
        // Insert data
        String insertQuery = "INSERT INTO users (id, username, password, akses) VALUES (?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(insertQuery);
        stmt.setInt(1, newId);
        stmt.setString(2, username);
        stmt.setString(3, password);
        stmt.setString(4, dbRole);
        stmt.executeUpdate();
        
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan dengan ID: " + newId);
        jTextField1.setText("");
        jPasswordField1.setText("");
        
        if (!isRegistrationMode) {
            jComboBox1.setSelectedIndex(0);
        }
        
        tampilkanDataUser();
        
        // Jika dalam mode pendaftaran, kembali ke login setelah pendaftaran berhasil
        if (isRegistrationMode) {
            new LoginForm().setVisible(true);
            dispose();
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        e.printStackTrace();
    }           
    }//GEN-LAST:event_jButton1ActionPerformed

